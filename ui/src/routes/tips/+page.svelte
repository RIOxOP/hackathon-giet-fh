    <h1 class="font bold text-2xl font-bold">
        Beware the Click: Spotting Fake Websites Before You Fall Victim
    </h1>
    <p>
        The internet is a vast and wondrous place, but it's also home to a host of dangers. Fake websites are one of the most common threats online, and they can be surprisingly difficult to spot. These sites are designed to look like legitimate businesses, but they're actually scams in disguise. They may steal your personal information, infect your computer with malware, or trick you into giving them money. Here are some tips to help you identify fake websites and protect yourself from online fraud:
    </p>
    <p>
        <span class="font-bold">Check the URL:</span> Before you click on a link, take a close look at the website's URL. Scammers often use URLs that are similar to legitimate sites, but with slight misspellings or extra characters. For example, "paypa1.com" instead of "paypal.com." If the URL looks suspicious, it's best to avoid the site altogether.
    </p>
    <p>
        <span class="font-bold">Look for the padlock:</span> Legitimate websites use encryption to protect your personal information. Look for a padlock icon in the address bar, or check that the URL starts with "https://" instead of "http://". If the site doesn't use encryption, it's not safe to enter your personal information.
    </p>
    <p>
        <span class="font-bold">Check for contact information:</span> Scammers often hide their contact information to avoid detection. Legitimate websites will have a physical address, phone number, and email address listed on their site. If you can't find any contact information, it's a red flag that the site may be fake.
    </p>
    <p>
        <span class="font-bold">Look for spelling and grammar mistakes:</span> Scammers often make mistakes in their spelling and grammar. If you notice a lot of errors on a website, it's likely a scam.
    </p>
